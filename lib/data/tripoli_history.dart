**TRIPOLI, LEBANON — VERIFIED HISTORICAL FACTS**

**Ancient Period**
- Evidence of settlement dates back to 1400 BCE.
- Phoenicians established a trading station in the 9th century BCE.
- Under Persian rule (around 300 BCE), Tripoli became the capital of a confederation of Phoenician city-states including Sidon, Tyre, and Arados Island.
- Under Hellenistic rule, Tripoli was used as a naval shipyard and enjoyed autonomy.
- Romans took control around 64 BCE.
- The 551 CE Beirut earthquake and tsunami destroyed the Byzantine city of Tripoli along with other Mediterranean coastal cities.

**Islamic Period (7th–11th century)**
- Tripoli was conquered by Muslims in 639/640 CE (18 AH).
- Under Umayyad rule, Tripoli became a commercial and shipbuilding center.
- Under Fatimid rule (from 969 CE), it achieved semi-independence and developed into a center of learning.
- Tripoli was famous for paper-making. The Fatimid-era library Dar al-'Ilm (House of Knowledge) held thousands of volumes.
- Banu Ammar ruled the city from 1070 until 1109. Under them, Tripoli became an intellectual center of Shiism with a library of 100,000 volumes.

**Crusader Era (1109–1289)**
- Crusaders besieged Tripoli from 1102; the city fell on July 12, 1109.
- Raymond de Saint-Gilles led the Crusader siege; the Citadel of Raymond de Saint-Gilles bears his name.
- The conquest caused extensive destruction, including the burning of Dar al-'Ilm.
- For ~180 years, Tripoli was the capital of the County of Tripoli (Crusader state).
- The County extended from Byblos to Latakia and included Krak des Chevaliers.
- The Great Mosque (Jami' al-Kabir) was originally the Cathedral of St. Mary of the Tower, built by Crusaders in the 12th century.
- Tripoli was a major silk-weaving center (4,000 looms) and exported citrus, sugar cane, and sugar.

**Mamluk Period (1289–1516)**
- In 1289, Sultan Qalawun (al-Mansur Qalawun) captured Tripoli from the Crusaders.
- The Mamluks destroyed the old port city and built a new inland city near the castle (~4 km from the old port).
- About 35 Mamluk monuments survive: mosques, madrasas, khans, hammams.
- The Citadel of Raymond de Saint-Gilles was restored and reused by the Mamluks (not rebuilt from scratch).
